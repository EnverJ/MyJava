package javasession;

public class LoopHW3 {

	public static void main(String[] args) {
		// Question 1: Write a program to print the following pattern using for
		// loop:
		for (int a = 0; a <= 3; a++) {
			for (int b =0; b <= 9; b++) {
				System.out.print(b+ " " +a);
			}
			System.out.println();
		}

	}

}
